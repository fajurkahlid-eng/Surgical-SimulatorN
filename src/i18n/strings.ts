export const strings: Record<Lang, StringsRecord> = { ar, en };

export function getString(lang: Lang, key: string): unknown {
  const keys = key.split('.');
  let v: unknown = strings[lang] ?? strings.ar;
  for (const k of keys) {
    v = (v as Record<string, unknown>)?.[k];
    if (v === undefined) return key;
  }
  return v;
}
